#Um usuário deseja converter a temperatura de Celsius para Fahrenheit. Escreva um programa que leia a temperatura em Celsius e exiba a temperatura 
#equivalente em Fahrenheit. 

print()
Celsius = float(input('Digite aqui a temperatura em graus Celsius: '))
print()

F = Celsius * 9 / 5 + 32
print(Celsius,'°C em Fahrenheit é de: ',F,'°F')
print()
                